export const CATEGORY_ORDER = [
  'Starters',
  'Rice specials',
  'Curries & gravy',
  'Drinks',
  'Desserts',
]

export function getSortedCategories(items = MENU, order = CATEGORY_ORDER) {
  const categories = [...new Set(items.map((m) => m.category))]
  categories.sort((a, b) => order.indexOf(a) - order.indexOf(b))
  return categories
}
